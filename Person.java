public class Person {
		
	private String firstname, lastname;

    public String getFN()
	{
		return firstname;
	}
	
	public String getLN()
	{
		return lastname;
	}
	
	public void setFN(String FirstName)
	{
		firstname= FirstName;
	}
	
	public void setLN(String LastName)
	{
		lastname= LastName;
	}
}